with CTE_CASE_1 as (
select count(*) as Total_count,
sum(
	case when transaction_response_code = '00' then 1 else 0 end
		) as Success_Count,
Sum(
	Case when transaction_response_code <> '00' then 1 else 0 end
		) as Failed_Count 		
        from fintech_transaction
)
select  
Success_Count / Total_count * 100 as success_rate,
Failed_Count / Total_count * 100 as Failed_rate from CTE_CASE_1;

