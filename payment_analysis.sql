SELECT 
TRANSACTION_RESPONSE_CODE,
COUNT(*) AS TRANSACTION_COUNT,
CASE WHEN TRANSACTION_RESPONSE_CODE = '51' THEN 'INSUFFICIENT FUND' 
	 WHEN TRANSACTION_RESPONSE_CODE = '00' THEN 'APPROVED TRANSACTION'
     WHEN TRANSACTION_RESPONSE_CODE = '05' THEN 'DO NOT HONOR'
     WHEN TRANSACTION_RESPONSE_CODE = '12' THEN 'INVALID TRANSACTION'
     WHEN TRANSACTION_RESPONSE_CODE = '54' THEN 'EXPIRED CARD'
     WHEN TRANSACTION_RESPONSE_CODE = '14' THEN 'INVALID CARD NUMBER'
     WHEN TRANSACTION_RESPONSE_CODE = '91' THEN 'SYSTEM ERROR' ELSE 'OTHER' END AS TRANSACTION_STATUS
    FROM FINTECH_TRANSACTION GROUP BY TRANSACTION_RESPONSE_CODE; 

